void drawChar(char ch, int x, int y, char c)
{
    if(ch == ' ') return;
    y += 6;
    for(int i = 0; i < 7; i++)
    {
        for(int j = 0; j < 5; j++)
        {
            if(!letters[ch-33][i][j]) continue;
            pixel(x + j, y - i, c);
        }
    }
}

void drawString(char* s, int x, int y, char c)
{
    int i = 0;
    int xoff = 0;
    int yoff = 0;
    while(s[i] != '\0')
    {
        if(s[i] != '\n') drawChar(s[i], x + xoff++*6, y-yoff*8, c);
        else
        {
            xoff=0;
            yoff++;
        }
        i++;
    }
}