#define clamp(v, mi, ma) MIN(MAX(v, mi), ma)

#define W 50
#define MS (W*W)
#define MAXCOL ('~'-' ')
uint8_t vBuff[MS+1] = { [0 ... MS-1] = '0', '\n' };

uint8_t get_pixel(uint8_t x, uint8_t y)
{
    if(x >= W || y >= W || x < 0 || y < 0) return 0;
    return vBuff[x + y * W] - ' ';
}
void pixel(uint8_t x, uint8_t y, uint8_t v)
{
    if(x >= W || y >= W || x < 0 || y < 0) return;
    vBuff[x + y * W] = MIN(' ' + v, '~');
}

void line(int x1, int y1, int x2, int y2, uint8_t c)
{
    int c1 = abs(x2 - x1);
    int c2 = abs(y2 - y1);
    float al = 0.0f;
    float inc = 1.0f;
    if((c1 > c2 && c1 != 0) || (c2 >= c1 && c2 != 0)) inc = 1.0f / (c1 > c2 ? c1 : c2);
    if((x1 > x2 && c1 > c2) || (y1 > y2 && c2 >= c1))
    {
        x1 += x2;
        x2 = x1 - x2;
        x1 -= x2;
        y1 += y2;
        y2 = y1 - y2;
        y1 -= y2;
    }
    if(c1 > c2)
    {
        for(int i = 0; i <= c1; i++)
        {
            pixel(i + x1, y1 * (1 - al) + y2 * al, c);
            al += inc;
        }
    }
    else
    {
        for(int i = 0; i <= c2; i++)
        {
            pixel(x1 * (1 - al) + x2 * al, i + y1, c);
            al += inc;
        }
    }
}

