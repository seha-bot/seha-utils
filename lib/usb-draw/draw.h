void drawChar(uint8_t c, uint8_t x, uint8_t y, uint8_t col)
{
    if(c == ' ') return;
    y += 6;
    for(int i = 0; i < 8; i++)
    {
        for(int j = 0; j < 8; j++)
        {
            if((slova[c][i] >> j) & 1) pixel(x+j, y-i, col);
        }
    }
}
void drawString(char* s, uint8_t x, uint8_t y, uint8_t col)
{
    int i = 0;
    while(s[i] != '\0')
    {
        drawChar(s[i], x+i*7, y, col);
        i++;
    }
}

